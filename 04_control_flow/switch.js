//-------------- Switch case stamenet
const month = 3

switch(month){

    case 1:
        console.log("Jan")
        break;
    case 2:
        console.log("Feb")
        break;
    case 3:
        console.log("March")
        break;
    case 4:
        console.log("Apr")
        break;
    case 5:
        console.log("May")
        break;
    case 6:
        console.log("June")
        break;

        default:
            console.log("no found");
        break;

}
//Need to implement break; because the condition will check and meet and print all the log without checking the other condition
// Case is case sensitive